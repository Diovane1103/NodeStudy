
this.add=function(a, b) {
    return a + ' + ' + b + ' = ' + (a + b)
}

this.sub=function(a, b) {
    return a + ' - ' + b + ' = ' + (a - b)
}

this.mul=function(a, b) {
    return a + ' * ' + b + ' = ' + (a * b)
}

this.div=function(a, b) {
    return a + ' / ' + b + ' = ' + (a / b)
}

